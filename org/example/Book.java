package org.example;

public class Book {
    public Book(String name, String author, Double price, int year, int num) {
        this.name = name;
        this.author = author;
        this.price = price;
        this.year = year;
        this.num = num;
    }
    public String name;
    public String author;
    public Double price;
    public Integer year;
    public Integer num;
}


